What is this?
</div>
<br/>
<img src="/core/img/site/cam.png" align="right"/>
<br/>
PiSos is a Linux/Raspberry Pi based security and remote monitoring project. PiSos enables authorized users to monitor and interact with remote locations via a web interface. PiSos allows remote users to : 
<br/>
<br/>
<ul>
<li>Monitor Live and Recorded CCTV from IP or USB cameras</li>
<li>Backup footage offsite to cloud based storage Dropbox</li>
<li>Connect new cameras with the web control panel remotely</li>
<li>Monitor Temperature sensors</li>
<li>Monitor PIR motion sensors</li>
<li>Monitor Smoke/Gas Detectos</li>
<li>Control Ac/Dc devices through Relays</li>
<li>Recieve/Setup Email and SMS Notifactions</li>
</ul>
