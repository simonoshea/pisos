What is this?
</div>
<br/>
<br/>
PiSos is a Linux/Raspberry Pi based security and remote monitoring project. PiSos allows users to monitor remote locations via a web interface. The web control panel interfaces with devices and sensors that are attached to the remote LAN or to the Raspberry Pi itself. The Raspberry Pi operates as a web server to host the web interface and as a host device for sensors, cctv cameras or other devices. SOSec allows remote users to : 
<br/>
<br/>
<ul>
<li>Monitor Live and Recorded CCTV from IP cameras or USB cameras</li>
<li>Backup footage offsite to cloud based storage service Dropbox</li>
<li>Connect new cameras with the web control panel remotely</li>
<li>Monitor Temperature sensors</li>
<li>Monitor PIR motion sensors</li>
<li>Monitor Smoke/Gas Detectos</li>
<li>Control AC/DC devices through Relays</li>
<li>Recieve/Setup Email Notifactions</li>
</ul>
